void 	cal_si(int *nbr, t_deftet *d, t_tet *t)
{
	int	i;
	int	j;

	i = 4*nbr[0];
	j = 1;
	while(j * j < i)
		j++;
	if (d || t)
		;
	nbr[1] = j;
}

void	g_init(t_gril g, int *nbr)
{
	int		i;
	int		j;

	i = -1;
	while (++i < nbr[1])
	{
		j = -1;
		while (++j < nbr[1])
			g[i][j] = '.';
		g[i][j] = '\n';
	}
}

void	affiche(t_gril g, int *nbr)
{
	int		i;

	i = -1;
	while (++i < nbr[1])
		write(1, g[i], nbr[1]+1);
}

